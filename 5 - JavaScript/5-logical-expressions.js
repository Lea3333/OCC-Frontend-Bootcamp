// Logical expressions
let sunny = true;
let warm = false;

console.log("Is is sunny AND warm?" , sunny && warm);

// true && true = true
// false && true = false
// true && false = false
// false && false = false

console.log("Is is sunny OR warm" , sunny || warm);

// true | | true = true
// false | | true = true
// true | | false = true
// false | | false = false
